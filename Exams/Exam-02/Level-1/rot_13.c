#include <io.h>

int main(int argc, char **argv) {
	if (argc == 2) {
		while (argv[1]) {
			if (argv[1][0] >= 'a' && argv[1][0] <= 'z') {
				argv[1][0] = (argv[1][0] - 'a' + 13) % 26 + 'a';
			}
			else if (argv[1][0] >= 'A' && argv[1][0] <= 'Z') {
				argv[1][0] = (argv[1][0] - 'A' + 13) % 26 + 'A';
			}
			write(1, argv[1][0], 1);
			argv[1]++;
		}
	}
	write(1, "\n", 1);
}
