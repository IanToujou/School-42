#include "../../include/libft.h"

/**
 * Looks for the last element in a linked list.
 *
 * @param lst The list to look for the last element.
 * @return The last element of the list.
 */
t_list	*ft_lstlast(t_list *lst)
{
	t_list	*tmp;

	if (!lst)
		return (NULL);
	tmp = lst;
	while (tmp->next)
		tmp = tmp->next;
	return (tmp);
}
