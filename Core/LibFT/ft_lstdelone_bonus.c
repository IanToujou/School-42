#include "libft.h"

/**
 * Deletes a given node of a linked list with
 * a specified function.
 *
 * @param lst The list do delete.
 * @param del A function to delete the content.
 */
void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!lst)
		return ;
	del(lst->content);
	free(lst);
}