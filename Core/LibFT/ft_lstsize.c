#include "libft.h"

/**
 * Gets the size of a linked list.
 *
 * @param lst The list to check the size of.
 * @return The size of the list.
 */
int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	while (lst)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
