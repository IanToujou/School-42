#include "libft.h"

/**
 * Writes a string to a given file descriptor.
 *
 * @param s The string to write.
 */
void	ft_putstr_fd(char const *s, int fd)
{
	int	i;

	i = 0;
	while (s[i])
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}
