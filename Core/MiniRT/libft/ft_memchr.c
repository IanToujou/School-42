#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t				i;
	const unsigned char	*ptr;

	ptr = (const unsigned char *)s;
	i = 0;
	while (i != n && ptr[i] != (unsigned char)c)
		i++;
	if (i == n)
		return (NULL);
	return ((void *)(ptr + i));
}
