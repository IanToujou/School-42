#include "../includes/ft.h"

void	ft_putnbr(int nb)
{
    long	n;

    n = nb;
    if (n < 0)
    {
        ft_putchar('-', 1);
        n *= -1;
    }
    if (n >= 10)
    {
        ft_putnbr(n / 10);
        ft_putnbr(n % 10);
    }
    else
    {
        ft_putchar(n + 48, 1);
    }
}
